// Função para obter total de referências
export function getTotalReferencias(): number {
  let total = 0;
  Object.values(bibliotecaReferencias).forEach(tema => {
    Object.values(tema).forEach(tipo => {
      total += tipo.length;
    });
  });
  return total;
}

// Função para obter referências por nível
export function getReferenciasPorNivel(nivel: "iniciante" | "intermediario" | "avancado"): Referencia[] {
  const refs: Referencia[] = [];
  Object.values(bibliotecaReferencias).forEach(tema => {
    Object.values(tema).forEach(tipo => {
      tipo.forEach(ref => {
        if (ref.nivel === nivel) refs.push(ref);
      });
    });
  });
  return refs;
}
